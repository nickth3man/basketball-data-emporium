const FALLBACK = { primary: "#777777", trim: "#CCCCCC" };

/** Picks the TruColor franchise color era in force for a given season start year.
 *  team_id is preferred because historical abbreviations are not globally unique
 *  (for example NYK can mean the Knicks or the New York Nets). */
export function colorForEra(
  abbreviation: string,
  year: number,
  teamId?: number,
): { primary: string; trim: string } {
  void abbreviation;
  const eras = teamId !== undefined ? TEAM_COLOR_ERAS_BY_TEAM_ID[teamId] : undefined;
  if (!eras || eras.length === 0) return FALLBACK;
  let best = eras[0];
  for (const era of eras) {
    if (era.from <= year) best = era;
  }
  return { primary: best.primary, trim: best.trim };
}
